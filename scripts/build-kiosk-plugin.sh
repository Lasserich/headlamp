#!/usr/bin/env bash
# =============================================================================
# build-kiosk-plugin.sh
#
# Fetches the headlamp-plugin-kiosk source from its remote repository, builds
# it, and writes a Kubernetes ConfigMap manifest (configmap-kiosk-plugin.yaml)
# in the root of this repo so it can be applied to the cluster.
#
# Usage:
#   ./scripts/build-kiosk-plugin.sh
#
# Requirements:
#   - git, node, npm (Node LTS recommended)
#
# TODO: Once headlamp-plugin-kiosk is published to ArtifactHub / npm, replace
#       this script with a pluginsManager entry in values.yaml (same pattern as
#       headlamp-flux).  The ConfigMap approach is a stop-gap for local dev.
# =============================================================================

set -euo pipefail

REPO_URL="https://github.tools.sap/openmcp/headlamp-plugin-kiosk.git"
WORK_DIR="$(mktemp -d)"
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "${SCRIPT_DIR}/.." && pwd)"
OUTPUT_FILE="${REPO_ROOT}/configmap-kiosk-plugin.yaml"

echo "==> Cloning headlamp-plugin-kiosk into ${WORK_DIR} ..."
git clone --depth 1 "${REPO_URL}" "${WORK_DIR}/headlamp-plugin-kiosk"

echo "==> Installing dependencies ..."
npm --prefix "${WORK_DIR}/headlamp-plugin-kiosk" ci

echo "==> Building plugin ..."
npm --prefix "${WORK_DIR}/headlamp-plugin-kiosk" run build

MAIN_JS="${WORK_DIR}/headlamp-plugin-kiosk/dist/main.js"
PACKAGE_JSON="${WORK_DIR}/headlamp-plugin-kiosk/package.json"

if [[ ! -f "${MAIN_JS}" ]]; then
  echo "ERROR: Build did not produce dist/main.js" >&2
  exit 1
fi

echo "==> Reading plugin metadata ..."
PLUGIN_NAME=$(node -e "process.stdout.write(require('${PACKAGE_JSON}').name)")
PLUGIN_VERSION=$(node -e "process.stdout.write(require('${PACKAGE_JSON}').version)")

# Extract just the fields Headlamp needs at runtime
PACKAGE_JSON_CONTENT=$(node -e "
const p = require('${PACKAGE_JSON}');
process.stdout.write(JSON.stringify({name: p.name, version: p.version}));
")

echo "==> Generating ConfigMap for ${PLUGIN_NAME}@${PLUGIN_VERSION} ..."

# Indent every line of main.js by 4 spaces for the YAML literal block scalar
MAIN_JS_INDENTED=$(sed 's/^/    /' "${MAIN_JS}")

cat > "${OUTPUT_FILE}" << YAML
# This file is auto-generated by scripts/build-kiosk-plugin.sh — do not edit by hand.
# Plugin: ${PLUGIN_NAME} v${PLUGIN_VERSION}
# Source: ${REPO_URL}
apiVersion: v1
kind: ConfigMap
metadata:
  name: kiosk-plugin
  namespace: headlamp
data:
  package.json: |-
    ${PACKAGE_JSON_CONTENT}
  main.js: |-
${MAIN_JS_INDENTED}
YAML

echo "==> Cleaning up temp directory ..."
rm -rf "${WORK_DIR}"

echo ""
echo "Done! Written to: ${OUTPUT_FILE}"
echo ""
echo "Apply with:"
echo "  kubectl apply -f ${OUTPUT_FILE}"
echo "  helm upgrade --install headlamp . -n headlamp"
